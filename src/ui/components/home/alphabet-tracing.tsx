const getCoordinates = (paths: string[]): [][] => {
  if (paths.length === 0) {
    return [];
  }
  let scale = 1;
  let numPoints = 400;
  let translateX = 0;
  let translateY = 0;
  const svgPaths = paths.map((pathString) => {
    const properties = new svgPathProperties(pathString);
    return {
      getTotalLength: () => properties.getTotalLength(),
      getPointAtLength: (fraction: number) =>
        properties.getPointAtLength(fraction),
    };
  });
  let flatCoords: [][] = pathsToCoords(
    svgPaths,
    scale,
    numPoints,
    translateX,
    translateY
  ) as [][];
  return flatCoords;
};

